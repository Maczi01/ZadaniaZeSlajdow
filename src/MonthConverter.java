public class MonthConverter {

    String convert(int month) {
        if(month == 1){
            return "Styczen";
        }
        if(month == 2){
            return "Luty";
        }
        if(month == 3){
            return "Marzec";
        }
        if(month == 4){
            return "Kwiecień";
        }
        if(month == 5){
            return "Maj";
        }
        if(month == 6){
            return "Czerwiec";
        }
        if(month == 7){
            return "Lipiec";
        }
        if(month == 8){
            return "Sierpien";
        }
        if(month == 10){
            return "Wrzesien";
        }
        if(month == 11){
            return "Pazdziernik";
        }
        if(month == 12){
            return "Grudzień";
        }
        return "Undefined";
        }
    }

